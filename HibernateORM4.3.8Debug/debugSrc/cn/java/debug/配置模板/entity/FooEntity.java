package cn.java.debug.配置模板.entity;

import javax.persistence.Entity;


@Entity(name="FooEntity")
public class FooEntity {
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
